package exercicios_java.classes;

public class ConversaoDeUnidadesDeTempo {
    public static double minutoParaSegundo(int valor){
        return valor * 60;
    }

    public static double horaParaMinuto(int valor){
        return valor * 60;
    }

    public static double diaParaHora(int valor){
        return valor * 24;
    }

    public static double semanaParaDia(int valor){
        return valor * 7;
    }

    public static double mesParaDia(int valor){
        return valor * 30;
    }

    public static double anoParaDia(int valor){
        return valor * 365;
    }
}
