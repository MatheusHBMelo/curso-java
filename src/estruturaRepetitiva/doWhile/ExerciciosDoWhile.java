package estruturaRepetitiva.doWhile;

public class ExerciciosDoWhile {
    public static void main(String[] args) {
        // 01
        int i=1;
        do{
            System.out.println(i);
            i++;
        }while(i<=10);

        //02
        // Declaring and initialization expression
        int ii = 1;

        // Do-while loop
        do {

            // Body of do-while loop
            // Print statement
            System.out.println("Hello World");

            // Update expression
            ii++;
        }

        // Test expression
        while (ii < 6);
    }
}
